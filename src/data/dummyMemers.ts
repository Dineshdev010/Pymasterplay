/**
 * Generates dynamic stats for dummy memers based on the current time seed.
 * This makes them appear to be active and changing rankings.
 */
export function getDynamicMemers() {
  const fiveMinSeed = Math.floor(Date.now() / (1000 * 60 * 5)); // Changes every 5 minutes
  
  return DUMMY_MEMERS.map((memer, index) => {
    // Generate some pseudo-random fluctuation
    const seed = fiveMinSeed + index;
    const xpFluctuation = Math.sin(seed * 0.7) * 800;
    const solvedFluctuation = Math.floor(Math.cos(seed * 0.9) * 8);
    const streakFluctuation = Math.floor(Math.sin(seed * 0.4) * 4);
    const walletFluctuation = Math.floor(Math.cos(seed * 1.2) * 200);

    return {
      ...memer,
      xp: Math.max(0, Math.floor(memer.baseXp + xpFluctuation)),
      problemsSolved: Math.max(0, memer.baseSolved + solvedFluctuation),
      streak: Math.max(0, memer.baseStreak + streakFluctuation),
      wallet: Math.max(0, memer.baseWallet + walletFluctuation),
      weeklyMomentum: Math.max(0, (memer.baseStreak + streakFluctuation) * 20 + 
                                  Math.min(memer.baseSolved + solvedFluctuation, 20) * 8 + 
                                  Math.floor((memer.baseXp + xpFluctuation) / 150)),
    };
  });
}
